#include <stdio.h>
/**
 * add - function
 * @a:parameter
 * @b:parameter
 * Return:result
 */
int add(int a, int b)
{
	return(a + b);
}

/**
 * sub - function
 * @a:parameter
 * @b:parameter
 * Return:result
 */
int sub(int a, int b)
{
	return(a - b);
}

/**
 * mul - function
 * @a:parameter
 * @b:parameter
 * Return:result
 */
int mul(int a, int b)
{
	return(a * b);
}

/**
 * div - function
 * @a:parameter
 * @b:parameter
 * Return:result
 */
int div(int a, int b)
{
	return(a / b);
}

/**
 * mod - function
 * @a:parameter
 * @b:parameter
 * Return:result
 */
int mod(int a, int b)
{
	return(a % b);
}
